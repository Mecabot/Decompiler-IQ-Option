    public static String convertToAlpha2(String str) {
        String str2 = null;
        if (str == null) {
            return null;
        }
        if (localeMap.containsKey(str)) {
            str2 = (String) localeMap.get(str);
        }
        return str2;
    }

    public static String convertToAlpha3(String str) {
        if (str == null) {
            return null;
        }
        for (Entry entry : localeMap.entrySet()) {
            if (((String) entry.getValue()).equals(str)) {
                return (String) entry.getKey();
            }
        }
        return null;
    }
